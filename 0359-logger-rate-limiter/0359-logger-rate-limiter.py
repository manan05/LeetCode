class Logger:

    def __init__(self):
        self.logger = {}    # {string:timestamp}

    def shouldPrintMessage(self, timestamp: int, message: str) -> bool:
        if message in self.logger:
            # print(self.logger)
            if self.logger[message] + 10 <= timestamp:
                self.logger[message] = timestamp
                return True
            return False
        else:
            self.logger[message] = timestamp
            return True


